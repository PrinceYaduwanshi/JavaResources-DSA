public class bitManipulate{
    public static void main(String[] args) {
        System.out.println("Binary AND " +(5 & 6));
        System.out.println("Binary OR " + (5 | 6));
        System.out.println("Binary XOR " + (5 ^ 6));
        System.out.println("Binary ONE'S COMPLIMENT " + (~5));
        System.out.println("Binary ONE'S COMPLIMENT " + (~0));
        System.out.println("Binary LEFT SHIFT " + (5 << 6));
        System.out.println("Binary RIGHT SHIFT " + (6 >> 1));
    }
}